<h1>Login Mandatory Pages</h1>
<p>LMP or "Login Mandatory Pages" plugin is a useful plugin to make pages accessible for only logged in users.</p>
<p>It also provides setting to users to modify text on pages and also users can set their "login" page link.</p>
<p>It is very easy to use. This plugin is mainly developed by the team of "Apex Web Guru".
